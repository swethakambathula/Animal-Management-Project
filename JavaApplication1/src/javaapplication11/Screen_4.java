    private void jButton_Exit1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_Exit1ActionPerformed
        // Search for an vaccine monitoring record to update
        if (jTextField_Search.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Deworming ID to Search");
            jTextField_Search.requestFocus();
            return;
        }

        try {
            String sql = "SELECT * FROM Deworming_Monitoring WHERE DewormingID  = ?";
            pst = conn.prepareStatement(sql);
            pst.setString(1, jTextField_Search.getText());
            rs = pst.executeQuery();

            if (rs.next()) {
                String add1 = rs.getString("DewormingID");
                jTextField_DewormingID.setText(add1);
                String add2 = rs.getString("AnimalID");
                jTextField_AnimalID.setText(add2);
                String add3 = rs.getString("DewormingDate");
                jTextField_DewormingDate.setText(add3);
                String add4 = rs.getString("DewormingRemarks");
                jTextField_DewormingRemarks.setText(add4);
                String add5 = rs.getString("NextDewormingDate");
                jTextField_NextDewormingDate.setText(add5);
                jTextField_Search.requestFocus();
            } else {
                JOptionPane.showMessageDialog(this, "The Deworming Record does not exist");
                jTextField_Search.requestFocus();

                //Clears the text fields
                jTextField_DewormingID.setText(null);
                jTextField_AnimalID.setText(null);
                jTextField_DewormingDate.setText(null);
                jTextField_DewormingRemarks.setText(null);
                jTextField_NextDewormingDate.setText(null);
                jTextField_Search.requestFocus();
            }
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton_Exit1ActionPerformed

