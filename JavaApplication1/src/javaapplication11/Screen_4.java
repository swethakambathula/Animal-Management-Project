    private void jButton_AddNewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_AddNewActionPerformed
        // Checks if textfields are empty
        if (jTextField_DewormingID.getText().isEmpty() || jTextField_AnimalID.getText().isEmpty()
                || jTextField_DewormingDate.getText().isEmpty() || jTextField_DewormingRemarks.getText().isEmpty()
                || jTextField_NextDewormingDate.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill the blank fields", "Blank Field", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            //insert query statement with values from jForm textfields
            String sql = "INSERT INTO Deworming_Monitoring (DewormingID, AnimalID, DewormingDate, DewormingRemarks, "
                    + "NextDewormingDate) "
                    + "VALUES(" + jTextField_DewormingID.getText() + ", '" + jTextField_AnimalID.getText() + "', "
                    + "'" + jTextField_DewormingDate.getText() + "', '" + jTextField_DewormingRemarks.getText() + "', "
                    + "'" + jTextField_NextDewormingDate.getText() + "')";

            pst = conn.prepareStatement(sql);
            pst.execute();

            //Message dialog on the screen
            JOptionPane.showMessageDialog(null, "New Vaccine Monitoring Record Successfully Added");

            //Clears the text fields after successful addition
            jTextField_Search.setText(null);
            jTextField_DewormingID.setText(null);
            jTextField_AnimalID.setText(null);
            jTextField_DewormingDate.setText(null);
            jTextField_DewormingRemarks.setText(null);
            jTextField_NextDewormingDate.setText(null);
        } //Exception handling
        catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(null, "Error adding new Deworming Monitoring");
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton_AddNewActionPerformed

    private void jButton_UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_UpdateActionPerformed
        // Update selected data
        if (jTextField_DewormingID.getText().isBlank()) {
            JOptionPane.showMessageDialog(null, "Please select record to be updated first");
            return;
        } else {

            int p = JOptionPane.showConfirmDialog(null, "Do you really want to UPDATE Deworming Info?", "Update", JOptionPane.YES_NO_OPTION);
            if (p == 0)
                
                try {
                String sql = "UPDATE Deworming_Monitoring SET AnimalID = '" + jTextField_AnimalID.getText() + "', "
                        + "DewormingDate = '" + jTextField_DewormingDate.getText() + "', "
                        + "DewormingRemarks= '" + jTextField_DewormingRemarks.getText() + "', "
                        + "NextDewormingDate= '" + jTextField_NextDewormingDate.getText() + "' "
                        + "WHERE DewormingID = '" + jTextField_DewormingID.getText() + "'";

                pst = conn.prepareStatement(sql);
                pst.execute();
                JOptionPane.showMessageDialog(null, "Deworming Monitoring Details Successfully UPDATED");
            } catch (SQLException | HeadlessException e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_jButton_UpdateActionPerformed

