    private void jButton_SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_SearchActionPerformed
        // Search for an vaccine monitoring record to update
        if (jTextField_Search.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Treatment ID to Search");
            jTextField_Search.requestFocus();
            return;
        }

        try {
            String sql = "SELECT * FROM Treatment_Monitoring WHERE TreatmentID = ?";
            pst = conn.prepareStatement(sql);
            pst.setString(1, jTextField_Search.getText());
            rs = pst.executeQuery();

            if (rs.next()) {
                String add1 = rs.getString("TreatmentID");
                jTextField_TreatmentID.setText(add1);
                String add2 = rs.getString("AnimalID");
                jTextField_AnimalID.setText(add2);
                String add3 = rs.getString("TreatmentDate");
                jTextField_TreatmentDate.setText(add3);
                String add4 = rs.getString("TreatmentDescription");
                jTextField_TreatmentDescription.setText(add4);
                String add5 = rs.getString("DiseaseTreated");
                jTextField_DiseaseTreated.setText(add5);
                String add6 = rs.getString("TreatmentComments");
                jTextField_TreatmentComments.setText(add6);
                jTextField_Search.requestFocus();
            } else {
                JOptionPane.showMessageDialog(this, "The Vaccine Record does not exist");
                jTextField_Search.requestFocus();

                //Clears the text fields
                jTextField_TreatmentID.setText(null);
                jTextField_AnimalID.setText(null);
                jTextField_TreatmentDate.setText(null);
                jTextField_TreatmentDescription.setText(null);
                jTextField_DiseaseTreated.setText(null);
                jTextField_TreatmentComments.setText(null);
                jTextField_Search.requestFocus();
            }
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton_SearchActionPerformed

