    private void jButton_AddNewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_AddNewActionPerformed
        // Checks if textfields are empty
        if (jTextField_TreatmentID.getText().isEmpty() || jTextField_AnimalID.getText().isEmpty()
                || jTextField_TreatmentDate.getText().isEmpty() || jTextField_TreatmentDescription.getText().isEmpty()
                || jTextField_DiseaseTreated.getText().isEmpty() || jTextField_TreatmentComments.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill the blank fields", "Blank Field", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            //insert query statement with values from jForm textfields
            String sql = "INSERT INTO Treatment_Monitoring (TreatmentID, AnimalID, TreatmentDate, TreatmentDescription, "
                    + "DiseaseTreated, TreatmentComments) "
                    + "VALUES(" + jTextField_TreatmentID.getText() + ", '" + jTextField_AnimalID.getText() + "', "
                    + "'" + jTextField_TreatmentDate.getText() + "', '" + jTextField_TreatmentDescription.getText() + "', "
                    + "'" + jTextField_DiseaseTreated.getText() + "', '" + jTextField_TreatmentComments.getText() + "')";

            pst = conn.prepareStatement(sql);
            pst.execute();

            //Message dialog on the screen
            JOptionPane.showMessageDialog(null, "New Treatment Monitoring Record Successfully Added");

            //Clears the text fields after successful addition
            jTextField_Search.setText(null);
            jTextField_TreatmentID.setText(null);
            jTextField_AnimalID.setText(null);
            jTextField_TreatmentDate.setText(null);
            jTextField_TreatmentDescription.setText(null);
            jTextField_DiseaseTreated.setText(null);
            jTextField_TreatmentComments.setText(null);
        } //Exception handling
        catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(null, "Error adding new Treatment Monitoring");
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton_AddNewActionPerformed

    private void jButton_UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_UpdateActionPerformed
        // Update selected data
        if (jTextField_TreatmentID.getText().isBlank()) {
            JOptionPane.showMessageDialog(null, "Please select record to be updated first");
            return;
        } else {

            int p = JOptionPane.showConfirmDialog(null, "Do you really want to UPDATE Treatment Info?", "Update", JOptionPane.YES_NO_OPTION);
            if (p == 0)
                
                try {
                String sql = "UPDATE Treatment_Monitoring SET AnimalID = '" + jTextField_AnimalID.getText() + "', "
                        + "TreatmentDate = '" + jTextField_TreatmentDate.getText() + "', "
                        + "TreatmentDescription = '" + jTextField_TreatmentDescription.getText() + "', "
                        + "DiseaseTreated = '" + jTextField_DiseaseTreated.getText() + "', "
                        + "TreatmentComments = '" + jTextField_TreatmentComments.getText() + "' "
                        + "WHERE TreatmentID = '" + jTextField_TreatmentID.getText() + "'";

                pst = conn.prepareStatement(sql);
                pst.execute();
                JOptionPane.showMessageDialog(null, "Treatment Monitoring Details Successfully UPDATED");
            } catch (SQLException | HeadlessException e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_jButton_UpdateActionPerformed

